#!/bin/bash

# ==============================================================================
# 脚本名称: ablation.sh
# 存放路径: ./scripts/ablation.sh
# 功能描述: 自动化执行 DS-MoME 的所有消融实验及对应测试
# ==============================================================================

# 【双重保险】：将本脚本的所有标准输出 (1) 和标准错误 (2) 同时打印到终端并追加到总日志文件中
exec > >(tee -a ./logs/ablation_test_fdmas.log) 2>&1

echo "================================================================="
echo "🚀 开始执行 DS-MoME 消融实验自动化流水线"
echo "🕒 开始时间: $(date "+%Y-%m-%d %H:%M:%S")"
echo "================================================================="

# 定义消融实验组别数组（严格按照你的顺序）
EXPERIMENTS=("A" "B" "C1" "C2" "C3" "D" "final")

# 定义全局超参数
EPOCHS=3
LR=5e-5
BATCH_SIZE=4
GPU_ID=1

# 循环遍历每一个消融实验组别
for EXP in "${EXPERIMENTS[@]}"; do
    echo ""
    echo "-----------------------------------------------------------------"
    echo "▶️ 当前正在进行实验组别: [ ${EXP} ]"
    echo "-----------------------------------------------------------------"

    # ---------------------------------------------------------
    # 第一步：运行训练 (调用 main.py)
    # ---------------------------------------------------------
    echo "⏳ [1/2] 开始训练 ${EXP} 组..."
    # 注意：这里的参数名（如 --epochs）请确保与你 main.py 中 argparse 定义的一致
    # 如果你代码里叫 --num_epochs，请把下面的 --epochs 改为 --num_epochs
    python main.py \
        --ablation ${EXP} \
        --num_epochs ${EPOCHS} \
        --lr ${LR} \
        --batch_size ${BATCH_SIZE} \
        --gpu_id ${GPU_ID}

    # 检查训练是否成功退出，如果不成功则中断整个脚本
    if [ $? -ne 0 ]; then
        echo "❌ 训练 [ ${EXP} ] 组时发生错误，自动化流水线已中止！"
        exit 1
    fi
    echo "✅ [ ${EXP} ] 组训练完成！"

    # ---------------------------------------------------------
    # 第二步：寻找刚刚生成的最佳权重文件
    # ---------------------------------------------------------
    # 根据我们之前写的逻辑，final 的权重在 final 文件夹，其他的在 ablation 文件夹
    if [ "${EXP}" == "final" ]; then
        WEIGHT_DIR="./weights/final"
    else
        WEIGHT_DIR="./weights/ablation"
    fi

    # 使用 ls -t (按时间排序) 和 head -n 1 来获取最新生成的带有该组别前缀的权重文件
    # 这样可以精准定位到刚刚训练完的那份权重
    BEST_WEIGHT=$(ls -t ${WEIGHT_DIR}/${EXP}_*.pt 2>/dev/null | head -n 1)

    if [ -z "${BEST_WEIGHT}" ]; then
        echo "⚠️ 警告：找不到 [ ${EXP} ] 组的权重文件，跳过测试步骤！"
        continue
    fi

    echo "🔍 找到最佳权重文件: ${BEST_WEIGHT}"

    # ---------------------------------------------------------
    # 第三步：运行测试 (调用 test_ds_fdmas.py)
    # ---------------------------------------------------------
    echo "⏳ [2/2] 开始测试 ${EXP} 组..."
    
    python test_ds_fdmas.py \
        --checkpoint "${BEST_WEIGHT}" \
        --gpu_id ${GPU_ID}

    if [ $? -ne 0 ]; then
        echo "❌ 测试 [ ${EXP} ] 组时发生错误！"
        # 测试报错不一定需要中断全部，这里选择继续下一个实验
    else
        echo "✅ [ ${EXP} ] 组测试完成！"
    fi

done

echo ""
echo "================================================================="
echo "🎉 所有消融实验及测试已全部执行完毕！"
echo "🕒 结束时间: $(date "+%Y-%m-%d %H:%M:%S")"
echo "📂 详细的 Python 级日志已分类保存在 ./logs/Train 和 ./logs/Test 中。"
echo "================================================================="